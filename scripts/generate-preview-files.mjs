/**
 * Reads all SolanaUI (sol/), shadcn (ui/), and utility source files
 * and generates a TypeScript module exporting them as a Sandpack file map.
 *
 * - Rewrites `@/` import aliases to `/` so Sandpack can resolve them
 * - Strips "use client" directives (not meaningful in Sandpack)
 *
 * Run: node scripts/generate-preview-files.mjs
 */

import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = new URL("..", import.meta.url).pathname;
const SOL_DIR = join(ROOT, "src/registry/sol");
const UI_DIR = join(ROOT, "src/components/ui");
const LIB_DIR = join(ROOT, "src/lib");
const OUT_FILE = join(ROOT, "src/lib/builder/preview-files.ts");

/**
 * Rewrite @/ aliases to / and strip "use client" directives.
 */
function transformSource(source) {
  return source
    .replace(/["']use client["'];?\s*\n?/g, "")
    .replace(/from\s+["']@\//g, 'from "/')
    .replace(/import\s+["']@\//g, 'import "/');
}

function readDir(dir, prefix) {
  const files = {};
  for (const name of readdirSync(dir).filter(
    (f) => f.endsWith(".tsx") || f.endsWith(".ts"),
  )) {
    const raw = readFileSync(join(dir, name), "utf-8");
    files[`${prefix}/${name}`] = transformSource(raw);
  }
  return files;
}

const solFiles = readDir(SOL_DIR, "/registry/sol");
const uiFiles = readDir(UI_DIR, "/components/ui");

// lib/utils.ts (imported as @/lib/utils by all components)
const REGISTRY_LIB_DIR = join(ROOT, "src/registry/lib");
const libFiles = {};
const utilsRaw = readFileSync(join(LIB_DIR, "utils.ts"), "utf-8");
libFiles["/lib/utils.ts"] = transformSource(utilsRaw);

// registry/lib/ files (imported as @/registry/lib/ by registry components)
for (const name of ["types.ts", "sort-utils.ts"]) {
  const raw = readFileSync(join(REGISTRY_LIB_DIR, name), "utf-8");
  libFiles[`/registry/lib/${name}`] = transformSource(raw);
}

// Build the file map
const allFiles = {
  ...solFiles,
  ...uiFiles,
  ...libFiles,
};

// Generate TypeScript source
const lines = [
  "// Auto-generated by scripts/generate-preview-files.mjs",
  "// Do not edit manually.",
  "",
  "const PREVIEW_FILES: Record<string, string> = {};",
  "",
];

for (const [path, content] of Object.entries(allFiles)) {
  // Use template literals to safely embed multi-line source code
  const escaped = content
    .replace(/\\/g, "\\\\")
    .replace(/`/g, "\\`")
    .replace(/\$\{/g, "\\${");
  lines.push(`PREVIEW_FILES["${path}"] = \`${escaped}\`;`);
  lines.push("");
}

lines.push("export { PREVIEW_FILES };");
lines.push("");

writeFileSync(OUT_FILE, lines.join("\n"), "utf-8");

const count = Object.keys(allFiles).length;
console.log(`Generated ${OUT_FILE} with ${count} files`);
